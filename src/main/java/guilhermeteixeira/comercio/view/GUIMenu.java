    private void jMenuItemEdicaoProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemEdicaoProdutoActionPerformed
        String nome = JOptionPane.showInputDialog("Informe o nome do produto que deseja editar:");

    if (nome != null && !nome.trim().isEmpty()) {
          Produto produto = new Produto();
        ArrayList<Produto> produtos = produto.pesquisar(nome);

        if (produtos.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Nenhum produto encontrado com o nome: " + nome);
        } else {
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("ID");
            modelo.addColumn("Nome");
            modelo.addColumn("ValorCompra");
            modelo.addColumn("Estoque");
            modelo.addColumn("Fonecedor");
            modelo.addColumn("TelefoneForne");
            modelo.addColumn("Preço");

            for (Produto p : produtos) {
                modelo.addRow(new Object[]{p.getId(), p.getNome(), p.getValorCompra(), p.getEstoque(), p.getFornecedor(), p.getTelefoneForne(), p.getPreco()});
            }

            jTablePesquisarPorNome.setModel(modelo);
            jInternalFramePesquisar.setVisible(true);
        }
    } else {
        JOptionPane.showMessageDialog(rootPane, "Digite um nome válido.");
    }
    }//GEN-LAST:event_jMenuItemEdicaoProdutoActionPerformed

